let index = 0;
let isTyping = false;

const box = document.getElementById("dialogueBox");
const typeSound = document.getElementById("typeSound");
const bgMusic = document.getElementById("bgMusic");
const image = document.getElementById("sceneImage");

function showDialogue(entry) {
  image.style.opacity = 0;

  setTimeout(() => {
    image.src = entry.image;
    image.style.opacity = 1;
  }, 200);

  typeText(entry.text, entry.sound);
}

document.body.addEventListener("click", () => {
  bgMusic.play().catch(() => {});
}, { once: true });

// Typewriter function
function typeText(text, soundSrc, i = 0) {
  if (i === 0) {
    box.textContent = "";
    isTyping = true;

    // change sound
    typeSound.src = soundSrc;
  }

  if (i < text.length) {
    box.textContent += text[i];

    // play sound
    typeSound.currentTime = 0;
    typeSound.play().catch(() => {});

    setTimeout(() => typeText(text, soundSrc, i + 1), 60);
  } else {
    isTyping = false;
  }
}

box.addEventListener("click", () => {
  if (isTyping) return;

  index++;
  if (index >= dialogue.length) index = 0;

  showDialogue(dialogue[index]);
});

function createPixel() {
  const pixel = document.createElement("div");
  pixel.classList.add("pixel");

  pixel.style.left = Math.random() * window.innerWidth + "px";
  pixel.style.animationDuration = (3 + Math.random() * 5) + "s";
  pixel.style.opacity = Math.random();

  document.body.appendChild(pixel);

  setTimeout(() => pixel.remove(), 8000);
}

setInterval(createPixel, 80);
